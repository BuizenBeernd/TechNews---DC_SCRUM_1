					<p>De nieuwe Channel Search-functie bouwt voort op Work Graph van het bedrijf, waarmee gebruikers het beste beschikbare kanaal 
					   kunnen vinden voor de informatie die ze nodig hebben, zonder al te veel te moeten weten over de specifieke paden. De nieuwe 
					   functie past resultaten nu voor iedere gebruiker aan op basis van de kanalen waar ze al actief in zijn.
					<br />
					<br />
					   De functie werkt vergelijkbaar met hoe Slack al gebruikt wordt. Een gebruiker typt een zoekterm in, waarna er resultaten verschijnen. 
					   Die informatie staat dan niet alleen in de tabbladen ‘bestanden’ en ‘berichten’, maar ook in het nieuwe ‘Kanalen’-tabblad. Daarin zijn 
					   de publieke en private kanalen waar de gebruiker in zit te zien. Ook is het mogelijk om de zoekopdracht te verbeteren of te filteren op 
					   basis van het kanaaltype of de werkruimte. Channel Search wordt al uitgerold naar desktop-gebruikers. Daarbij vraagt Slack om feedback 
					   op de ervaring van zijn gemeenschap.<p>
					
					<h5>Encryptie-tool</h5>
					<p>Slack kondigde vorige maand ook een eigen encryptie-tool aan, waarmee bedrijven onder meer correspondentie tussen werknemers kunnen versleutelen 
					   met behulp van eigen encryptiesleutels. De dienst heet Slack EKM. Alle data die gebruikers via het platform verzenden wordt al versleuteld door 
					   Slack zelf, maar grote bedrijven willen vaak ook de optie hebben om dat proces zelf te beheren. In industrieën die met veel en strikte regelgeving 
					   te maken hebben is dit een veelvoorkomende eis. Denk daarbij bijvoorbeeld aan de financiële sector en de gezondheidszog.
					<br />
					<br />
					   Slack EKM is ook te gebruiken om de manier waarop werknemers intern data delen te controleren. De tool geeft bedrijven verder de optie om gebruiken 
					   en chatkanalen te blokkeren waarbinnen gevoelige informatie is misbruikt of daar verkeerd mee is omgegaan. Specifieke berichten en bestanden kunnen 
					   ook teruggetrokken worden.</p>
