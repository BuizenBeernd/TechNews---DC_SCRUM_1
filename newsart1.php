					<p>Een nieuwe maand, nieuwe updateperikelen voor Windows. De recentste problemen treffen systemen met Windows 7 
					   en Windows 8.1. Wie één van die besturingssystemen draait, gecombineerd met een beveiligingsoplossing van Sophos 
					   of Avast, ontdekt dat zijn pc niet meer opstart na de installatie van de recentste updates.<p>
					
					<h5>Gedaan met opstarten</h5>
					<p>De schuldigen zijn de maandelijkse beveiligingsupdates KB4493467, KB4493446, KB4493448, KB4493472, KB4493450 en KB4493451. 
					   Die stelt Microsoft sinds dinsdag beschikbaar als Patch Tuesday-routine-updates. Voor de meeste gebruikers werken deze 9 
					   april-updates gewoon, maar wanneer ze geïnstalleerd worden op een systeem met Sophos Endpoint Security and Control of Sophos 
					   Endpoint Central/Advanced ontstaat er een probleem dat er voor zorgt dat gebruikers hun pc niet meer kunnen opstarten.
					<br />
					<br />
					   De problemen doen zich ook voor op Windows Server 2008 R2 en 2012. Volgens online berichten lopen gebruikers van beveiligingssoftware 
					   Avast tegen heel gelijkaardige problemen aan. Sophpos raadt gebruikers aan de update niet te installeren en Microsoft heeft de uitrol 
					   van de updates intussen gestopt voor kwetsbare systemen met daarop software van Sophos. Naar Avast-gebruikers blijven de updates vooralsnog 
					   wel gewoon uitrollen.
					
					<h5>Zakelijke slachtoffers</h5>
					<p>Omdat het om beveiligingsupdates gaat en geen grote Windows-updates, is de kans echter groot dat voor velen het kwaad al is geschied. De update 
					   lijkt vooral schade aan te richten in zakelijke omgevingen. De beveiliging van Sophos is populair in professionele context en ook de getroffen 
					   versies van Avast focussen vooral op de zakelijke gebruiker. De problemen doen zich immers voor met Avast for Business en Avast Cloud Care, zo 
					   meldt de beveiligingsspecialist zelf.
					<br />
					<br />
					   Wie geconfronteerd wordt met een systeem dat niet meer opstart, kan zich in principe een weg banen uit de miserie door zijn of haar systeem op te 
					   starten in veilige modus. Van daaruit moet eerst de antivirusoplossing worden verwijderd. Vervolgens is een standaard-boot in principe opnieuw mogelijk. 
					   Zodra dat is gelukt, dienen de slechte updates verwijderd te worden, en kan je de Endpoint-beveiliging opnieuw installeren.
					   
					<h5>Slecht getest</h5>
					<p>Microsoft oogst de laatste tijd veel kritiek door Windows-updates die meer problemen veroorzaken dan ze oplossen. In dit geval gaat het gelukkig niet om 
					   Windows 10, al heeft Windows 7 nog bijna evenveel gebruikers. Het percentage aan Windows 8.1-gebruikers is dan weer marginaal, zeker in een zakelijke omgeving. 
					   De problemen tonen opnieuw aan dat Microsoft updates onvoldoende test voordat ze uitgerold worden, ondanks beweringen van het tegendeel. Zeker in dit geval zijn 
					   de problemen onvergeeflijk, aangezien ze het resultaat zijn van een conflict met veelgebruikte beveiligingssoftware. Hoewel een snelle installatie van beveilingspatches 
					   in theorie altijd aan te raden valt, lijkt het in een professionele omgeving een goed idee om ook met beveiligingsupdates de kat enkele dagen uit de boom te kijken.</p>
