					<p>Volgens ITProToday hebben eerdere generaties van cold, deep cold of icy storage tiers en klassen veel vertragingen opgelopen. Ze vereisten 
					   dikwijls meerdere stappen vergelijkbaar met traditioneel hiërarchisch opslagbeheer. Google verandert die aanpak nu en biedt onmiddellijke, 
					   low-latency toegang tot content.
					<br />
					<br />
					   Ice cold storage, een klasse van gearchiveerde cloudopslag, is ontwikkeld voor databehoud op lange termijn. Ze gebruiken dezelfde API’s als 
					   Google voor andere opslagmethodes op vlak van toegang en management. Hiermee volgt de zoekgigant de industrietrends en staat het opnieuw op 
					   de kaart. Prijzen starten vanaf 1,23 dollar per TB per maand.<p>
					
					<h5>Beschikbaarheid</h5>
					<p>Die startprijs bevat niet de extra kosten voor toegang, dataverhuis en andere metadata operaties. Het is tevens ook belangrijk om te weten 
					   hoelang het duurt voordat je databestelling klaar is en hoe snel je data kan verhuizen. Daarover geeft Google voorlopig nog geen details vrij. 
					   Naast ice cold storage lanceert Google Cloud Filestore, een managed file storage systeem. De oplossing is vandaag beschikbaar en heeft een 
					   leessnelheid tot 1,2 GB/s aan 60K IOPS. Dankzij toegang tot een eigen NFS cloud opslagdienst voegt Google zich toe aan het rijtje cloudproviders 
					   die hun cloud opslagplatformen steeds meer compatibel maken met applicaties van de klant.</p>
					
					<h5>Regional Persistent Disk</h5>
					<p>Google stelt voortaan ook Regional Persistent Disk beschikbaar. Hiermee kan active-active synchronous disk replication over twee zones worden voorzien 
					   binnen dezelfde regio. Door de opslag geografisch dichter bij elkaar te brengen waar de applicaties ze nodig hebben, verwacht Google betere betrouwbaarheid 
					   en hogere prestaties.
					<br />
					<br />
					   Het mikt op lees- en schrijfsnelheden van 240 MB/s en het aantal Persistent Disks die aan virtuele machines kunnen worden gekoppeld stijgt naar 128. Verder 
					   biedt Google nog V4 signature-ondersteuning toe voor Google Cloud storage waardoor gebruikers toegang hebben tot meerdere object stores binnen dezelfde 
					   applicatiecode.</p>
